// IF, ELSE IF , ELSE

//IF MY GRADE GREATER THAN 80 I GOT A
const grade = 90 ;

if (grade >= 80) {
    console.log("Congrats You've Got 'A'")
} 

else if (grade <= 80) {
    console.log("Cool You've Got 'B'")
}

else if (grade >= 65) {
    console.log("Okay You've Got 'C'")
}

else {
    console.log("Your Grade Below 'C' Keeps Learning")
}

